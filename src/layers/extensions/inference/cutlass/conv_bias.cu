at::Tensor conv_bias(const int sm, const at::Tensor& feature, const at::Tensor& weight,
                     const at::Tensor& bias, const int stride, at::Tensor& out_buf)
{
    const int kernel = weight.size(2);
    if (sm < 75) {
        auto out = at::conv2d(feature, weight, at::nullopt, stride, (kernel - 1) / 2);
        return out + bias.reshape({ 1, -1, 1, 1 });
    }
    auto launch_cutlass = [&](auto sm_v) {
        using sm_t = decltype(sm_v);
        return conv_bias_generic_cutlass<sm_t>(out_buf, feature, weight, bias, stride);
    };
    const int H = feature.size(2);
    const int W = feature.size(3);
    const int C2 = weight.size(0);
    const int C1 = weight.size(1);
    assert(kernel >= 2 && kernel <= 3 && stride >= 1 && stride <= 2);
    return launch_cutlass_helper<decltype(launch_cutlass), true>(sm, launch_cutlass, HINT_MAP_CONV_BIAS,
                                                                 HINT_MAP_CONV_BIAS_NAME, C2, C1, H,
                                                                 W, kernel - 2, stride - 1);
}
