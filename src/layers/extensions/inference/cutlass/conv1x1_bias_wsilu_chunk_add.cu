at::Tensor conv1x1_bias_wsilu_chunk_add(const int sm, const at::Tensor& feature,
                                        const at::Tensor& weight, const at::Tensor& bias,
                                        const at::Tensor& weight0, const at::Tensor& weight1,
                                        const at::Tensor& weight2, const at::Tensor& weight3,
                                        const at::Tensor& bias0, const at::Tensor& bias1,
                                        const at::Tensor& bias2, const at::Tensor& bias3,
                                        at::Tensor& out_buf)
{
    if (sm < 75) {
        auto out = at::conv2d(feature, weight);
        out = out + bias.reshape({ 1, -1, 1, 1 });
        out = out * at::sigmoid(out * 4.f);
        auto x1 = out.index({ at::indexing::Slice(), at::indexing::Slice(0, at::nullopt, 4),
                              at::indexing::Slice(), at::indexing::Slice() });
        auto x2 = out.index({ at::indexing::Slice(), at::indexing::Slice(1, at::nullopt, 4),
                              at::indexing::Slice(), at::indexing::Slice() });
        auto x3 = out.index({ at::indexing::Slice(), at::indexing::Slice(2, at::nullopt, 4),
                              at::indexing::Slice(), at::indexing::Slice() });
        auto x4 = out.index({ at::indexing::Slice(), at::indexing::Slice(3, at::nullopt, 4),
                              at::indexing::Slice(), at::indexing::Slice() });
        return x1 + x2 + x3 + x4;
    }
    auto launch_cutlass = [&](auto sm_v) {
        using sm_t = decltype(sm_v);
        return conv1x1_bias_generic_cutlass<sm_t, true, 0, false, true, true>(
            out_buf, feature, weight, bias, at::nullopt, at::nullopt, at::nullopt, weight0, weight1,
            weight2, weight3, bias0, bias1, bias2, bias3);
    };
    const int H = feature.size(2);
    const int W = feature.size(3);
    const int C2 = weight.size(0);
    const int C1 = weight.size(1);
    return launch_cutlass_helper(sm, launch_cutlass, HINT_MAP_BIAS_WSILU_CHUNK_ADD,
                                 HINT_MAP_BIAS_WSILU_CHUNK_ADD_NAME, C2, C1, H, W);
}
