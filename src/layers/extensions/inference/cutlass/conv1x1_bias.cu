at::Tensor conv1x1_bias(const int sm, const at::Tensor& feature, const at::Tensor& weight,
                        const at::Tensor& bias, at::Tensor& out_buf)
{
    if (sm < 75) {
        auto out = at::conv2d(feature, weight);
        return out + bias.reshape({ 1, -1, 1, 1 });
    }
    auto launch_cutlass = [&](auto sm_v) {
        using sm_t = decltype(sm_v);
        return conv1x1_bias_generic_cutlass<sm_t, true>(out_buf, feature, weight, bias);
    };
    const int H = feature.size(2);
    const int W = feature.size(3);
    const int C2 = weight.size(0);
    const int C1 = weight.size(1);
    return launch_cutlass_helper(sm, launch_cutlass, HINT_MAP_BIAS, HINT_MAP_BIAS_NAME, C2, C1, H, W);
}
