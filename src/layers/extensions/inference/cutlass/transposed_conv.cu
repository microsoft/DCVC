at::Tensor transposed_conv(const int sm, const at::Tensor& feature, const at::Tensor& weight,
                           const int stride, at::Tensor& out_buf)
{
    const int kernel = weight.size(2);
    if (sm < 75) {
        return at::conv_transpose2d(feature, weight, at::nullopt, stride);
    }
    auto launch_cutlass = [&](auto sm_v) {
        using sm_t = decltype(sm_v);
        return transposed_conv_generic_cutlass<sm_t>(out_buf, feature, weight, stride);
    };
    const int H = feature.size(2);
    const int W = feature.size(3);
    const int C2 = weight.size(0);
    const int C1 = weight.size(1);
    assert(kernel == stride && kernel == 2);
    return launch_cutlass_helper<decltype(launch_cutlass), true>(
        sm, launch_cutlass, HINT_MAP_TRANSPOSED_CONV, HINT_MAP_TRANSPOSED_CONV_NAME, C2, C1, H, W);
}
